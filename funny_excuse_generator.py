import random

reasons = [
    "Мой кот случайно",
    "Будильник неожиданно",
    "Дорога на работу внезапно",
    "Инопланетяне на секунду",
    "Мой компьютер внезапно",
    "Соседский попугай случайно"
]

actions = [
    "устроил пожар в ванной",
    "перемотал время назад",
    "исчез в параллельной вселенной",
    "запер меня в квартире",
    "захватил интернет-соединение",
    "объявил меня своим богом"
]

endings = [
    "и мне пришлось разбираться.",
    "поэтому я не смог выйти вовремя.",
    "и это заняло больше времени, чем ожидалось.",
    "так что мне пришлось объяснять это полиции.",
    "и теперь я немного опоздал.",
    "но, к счастью, всё закончилось хорошо."
]

def generate_excuse():
    return f"{random.choice(reasons)} {random.choice(actions)} {random.choice(endings)}"

print("Почему я опоздал:", generate_excuse())